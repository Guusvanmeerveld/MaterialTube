import Video from "@interfaces/api/trending";

interface Channel {
	author: string;
	authorId: string;
	authorUrl: string;
	authorBanners: AuthorBanner[];
	authorThumbnails: AuthorBanner[];
	subCount: number;
	totalViews: number;
	joined: number;
	autoGenerated: boolean;
	isFamilyFriendly: boolean;
	description: string;
	descriptionHtml: string;
	allowedRegions: string[];
	latestVideos: Video[];
	relatedChannels: RelatedChannel[];
}

interface AuthorBanner {
	url: string;
	width: number;
	height: number;
	quality?: Quality;
}

enum Quality {
	Default = "default",
	End = "end",
	High = "high",
	Maxres = "maxres",
	Maxresdefault = "maxresdefault",
	Medium = "medium",
	Middle = "middle",
	Sddefault = "sddefault",
	Start = "start"
}

interface RelatedChannel {
	author: string;
	authorId: string;
	authorUrl: string;
	authorThumbnails: AuthorBanner[];
}

export default Channel;
